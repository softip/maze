    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        creature.updateState();
        List<Thing> coisas = creature.getThingsInVision();
        for (Thing coisa : coisas) {
            double distancia = creature.calculateDistanceTo(coisa);
            double angulo = creature.calculateAngleTowards(coisa);
            System.out.println("Nome " + coisa.getAttributes().getColor());
            System.out.println("Dist√¢ncia " + distancia);
            System.out.println("Angulo " + Math.toDegrees(angulo-creature.getPitch()));
            System.out.println("");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

